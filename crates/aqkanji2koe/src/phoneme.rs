/// カタカナのモーラ文字列をローマ字音声記号に変換する。
///
/// 読み記号表に準拠した正規形を返す。
/// ッ は "xtu" を返す（呼び出し側で子音連続に変換する）。
/// 不明なモーラは空文字列を返す。
pub fn katakana_mora_to_roman(mora: &str) -> &'static str {
    match mora {
        // ── 2文字モーラ ──────────────────────────────────────────────────────
        "キャ" => "kya",  "キュ" => "kyu",  "キェ" => "kye",  "キョ" => "kyo",
        "シャ" => "sha",  "シュ" => "shu",  "シェ" => "she",  "ショ" => "sho",
        "チャ" => "cha",  "チュ" => "chu",  "チェ" => "che",  "チョ" => "cho",
        "ニャ" => "nya",  "ニュ" => "nyu",  "ニェ" => "nye",  "ニョ" => "nyo",
        "ヒャ" => "hya",  "ヒュ" => "hyu",  "ヒェ" => "hye",  "ヒョ" => "hyo",
        "ミャ" => "mya",  "ミュ" => "myu",  "ミェ" => "mye",  "ミョ" => "myo",
        "リャ" => "rya",  "リュ" => "ryu",  "リェ" => "rye",  "リョ" => "ryo",
        "ギャ" => "gya",  "ギュ" => "gyu",  "ギェ" => "gye",  "ギョ" => "gyo",
        "ジャ" => "ja",   "ジュ" => "ju",   "ジェ" => "je",   "ジョ" => "jo",
        "ヂャ" => "dya",  "ヂュ" => "dyu",  "ヂェ" => "dye",  "ヂョ" => "dyo",
        "ビャ" => "bya",  "ビュ" => "byu",  "ビェ" => "bye",  "ビョ" => "byo",
        "ピャ" => "pya",  "ピュ" => "pyu",  "ピェ" => "pye",  "ピョ" => "pyo",
        // ヴ行
        "ヴァ" => "va",   "ヴィ" => "vi",   "ヴェ" => "ve",   "ヴォ" => "vo",
        // ツ行拡張
        "ツァ" => "tsa",  "ツィ" => "tsi",  "ツェ" => "tse",  "ツォ" => "tso",
        // ス行拡張
        "スィ" => "swi",
        // テ行拡張
        "ティ" => "thi",  "テュ" => "thu",
        // ト行拡張
        "トゥ" => "twu",
        // ファ行
        "ファ" => "fa",   "フィ" => "fi",   "フェ" => "fe",   "フォ" => "fo",
        // ズ行拡張
        "ズィ" => "zwi",
        // デ行拡張
        "ディ" => "dhi",  "デュ" => "dhu",
        // ド行拡張
        "ドゥ" => "dwu",
        // ウ行拡張
        "ウィ" => "wi",   "ウェ" => "we",   "ウォ" => "wo",
        // イェ
        "イェ" => "ye",

        // ── 1文字モーラ ──────────────────────────────────────────────────────
        // 母音
        "ア" => "a",  "イ" => "i",  "ウ" => "u",  "エ" => "e",  "オ" => "o",
        // カ行
        "カ" => "ka", "キ" => "ki", "ク" => "ku", "ケ" => "ke", "コ" => "ko",
        // サ行
        "サ" => "sa", "シ" => "si", "ス" => "su", "セ" => "se", "ソ" => "so",
        // タ行
        "タ" => "ta", "チ" => "ti", "ツ" => "tu", "テ" => "te", "ト" => "to",
        // ナ行
        "ナ" => "na", "ニ" => "ni", "ヌ" => "nu", "ネ" => "ne", "ノ" => "no",
        // ハ行
        "ハ" => "ha", "ヒ" => "hi", "フ" => "hu", "ヘ" => "he", "ホ" => "ho",
        // マ行
        "マ" => "ma", "ミ" => "mi", "ム" => "mu", "メ" => "me", "モ" => "mo",
        // ヤ行
        "ヤ" => "ya", "ユ" => "yu", "ヨ" => "yo",
        // ラ行
        "ラ" => "ra", "リ" => "ri", "ル" => "ru", "レ" => "re", "ロ" => "ro",
        // ワ行
        "ワ" => "wa", "ヲ" => "o",
        // ガ行
        "ガ" => "ga", "ギ" => "gi", "グ" => "gu", "ゲ" => "ge", "ゴ" => "go",
        // ザ行
        "ザ" => "za", "ジ" => "zi", "ズ" => "zu", "ゼ" => "ze", "ゾ" => "zo",
        // ダ行
        "ダ" => "da", "ヂ" => "di", "ヅ" => "du", "デ" => "de", "ド" => "do",
        // バ行
        "バ" => "ba", "ビ" => "bi", "ブ" => "bu", "ベ" => "be", "ボ" => "bo",
        // パ行
        "パ" => "pa", "ピ" => "pi", "プ" => "pu", "ペ" => "pe", "ポ" => "po",
        // ヴ
        "ヴ" => "vu",
        // 特殊
        "ン" => "nn",
        "ー" => "-",
        "ッ" => "xtu", // 子音連続処理は呼び出し側で行う
        // 不明
        _ => "",
    }
}

/// 指定したローマ字モーラに対して、促音連続のために冒頭子音を返す。
///
/// 例: "ka" → Some('k'), "sha" → Some('s'), "chi" → Some('c')
/// 先頭が母音のモーラは None を返す (促音+母音 は "xtu" フォールバック)。
pub fn get_doubling_consonant(roman: &str) -> Option<char> {
    let first = roman.chars().next()?;
    match first {
        'a' | 'i' | 'u' | 'e' | 'o' => None, // 母音始まり
        c => Some(c),
    }
}

/// そのモーラが無声化可能かどうかを返す。
/// 無声化可能な場合、ローマ字の `_` 付き表現を返す。
///
/// 仕様書「無声化読み記号」に列挙された音節のみが対象。
pub fn devoiced_roman(mora: &str) -> Option<&'static str> {
    match mora {
        "キ"   => Some("_ki"),
        "ク"   => Some("_ku"),
        "ヒ"   => Some("_hi"),
        "フ"   => Some("_fu"),
        "フィ" => Some("_fi"),
        "シ"   => Some("_si"),
        "シュ" => Some("_syu"),
        "ス"   => Some("_su"),
        "スィ" => Some("_swi"),
        "ティ" => Some("_thi"),
        "トゥ" => Some("_twu"),
        "ピ"   => Some("_pi"),
        "プ"   => Some("_pu"),
        "チ"   => Some("_ti"),
        "チュ" => Some("_tyu"),
        "ツ"   => Some("_tu"),
        "ツィ" => Some("_tsi"),
        _ => None,
    }
}

/// 無声化可能なモーラについて、かな記法での `_カタカナ` 形式を返す。
pub fn devoiced_kana(mora: &str) -> Option<String> {
    // 無声化表記はカタカナ + アンダーバープレフィックス
    devoiced_roman(mora).map(|_| format!("_{mora}"))
}
